package collections;

public class Gain {

    private String prenom;
    private Integer benefice;

    public Gain(String prenom, Integer benefice) {
        this.prenom = prenom;
        this.benefice = benefice;
    }

    public String getPrenom() {
        return prenom;
    }

    public Integer getBenefice() {
        return benefice;
    }
}
